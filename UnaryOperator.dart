void main(){

  //postfix
  print('postfix increment');

  var x = 10;
  print(x++);
  print(x++);
  print(x++);
  print(x++);
  print(x++);

  //prefix
  print('prefix increment');

  var y = 20;
  print(++y);
  print(++y);
  print(++y);
  print(++y);
  print(++y);




  //postfix
  print('postfix decrement');

  var z = 10;
  print(z--);
  print(z--);
  print(z--);
  print(z--);
  print(z--);

  //prefix
  print('prefix increment');

  var u = 20;
  print(--u);
  print(--u);
  print(--u);
  print(--u);
  print(--u);



}