void main(){

  var x = 20;
  print(x++);   //postfix = It returns the actual value of operand before increment.

  var y = 20;
  print(++y);   //prefix = It increment the value of operand.


  var z = 20;
  print(z--);   //postfix = It returns the actual value of operand before increment.

  var yz = 20;
  print(--yz);   //prefix = It decrement the value of operand.

}